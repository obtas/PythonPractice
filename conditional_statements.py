# If/Else conditional statements

x = 31

if x % 4 == 0:
    print("This number is divisible by 4")
else:
    print("This number is not divisible by 4")

# If Elif Else

n = 15
if n % 3 == 0:
    print("Number divisible by 3")
elif n % 4 ==0:
    print("Number divisible by 4")
else:
    print("Number not divisible by 3 and 4")